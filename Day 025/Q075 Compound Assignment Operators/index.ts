// This function uses compound assignment operators for operations
function compoundAssignment(){
    let x = 4 // Starts with x equal to 4
    console.log(`Initial value :`, x);
    x+=3
    console.log(`Addition :`,x); // Shows x after addition
    x-=1
    console.log(`Subtraction :`,x); // Shows x after subtraction
    x*=5
    console.log(`Multiplication :`,x); // Shows x after multiplication
    x/=2
    console.log(`Division :`,x); // Shows x after division
}
compoundAssignment()
// We simplify arithmetic operations on x using 
// compound assignment operators.