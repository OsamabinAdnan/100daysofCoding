"use strict";
// This function shows how to assign and update variable values
function bio() {
    let myName = "Osama"; // Initially assigns
    console.log("Initial Value: ", myName); // Logs the initial value
    myName = "Arham"; // Updates the value
    console.log("Updated Value: ", myName); // Logs the updated value    
}
bio();
// We assign a value to a variable and then update it,
// showing how values can change.
