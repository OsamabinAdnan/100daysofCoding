// This function changes a string to uppercase and then to lowercase
function converting (str:string){
    let upperCase = str.toUpperCase(); // Converts to uppercase
    let lowerCase = str.toLowerCase(); // Converts to lowercase
   console.log(`\nUpperCase: `, upperCase, `LowerCase: `, lowerCase);
   
    
}
// We're showing the same text in all uppercase and then all lowercase.
converting("Osama bin Adnan");
