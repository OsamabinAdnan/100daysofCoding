"use strict";
// Reserved word: let - used to declare a block-scoped 
// local variable
let count = 5;
//Reserved word: if - used to execute a block of code if a condition is true
if (count > 0) {
    console.log("Count is greater than 0.");
}
// Reserved word: function and return - used to define a function and used to exit a function and return a value from that function respectively
function add(a, b) {
    return a + b;
}
console.log(add(6, 4));
// Demonstrates valid use cases for the reserved 
// words 'let', 'if', 'function' and 'return'.
