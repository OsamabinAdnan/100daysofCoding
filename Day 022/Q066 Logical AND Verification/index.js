"use strict";
// This function sees if both inputs are true
function logicalVerification(num1, num2) {
    // Only says true if both val1 and val2 are true
    return num1 && num2;
}
// Trying it with true and false
console.log(logicalVerification(true, true)); //Shows true
console.log(logicalVerification(true, false)); //Show false
